#include "libft.h"

int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int a;

	a = 1;
	while (*s1++ == *s2++)
	{
		if (a == n)
			return (0);
		if (*s1 == '\0' && *s2 == '\0')
			return (0);
		a++;
	}
	return (*--s1 - *--s2);
}
