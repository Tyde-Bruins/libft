#include "libft.h"

char	*ft_strncpy(char *dst, char *src, unsigned int n)
{
	int	a;

	a = 0;
	while ((src[a] != '\0') && a < n)
	{
		dst[a] = src[a];
		a++;
	}
	dst[a] = '\0';
	return (dst);
}
